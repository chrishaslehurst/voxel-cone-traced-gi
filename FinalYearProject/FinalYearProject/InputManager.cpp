#include "InputManager.h"

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

InputManager::InputManager()
{

}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

InputManager::~InputManager()
{

}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void InputManager::Initialise()
{
	for (int i = 0; i < 256; i++)
	{
		m_keys[i] = false;
	}
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void InputManager::KeyDown(unsigned key)
{
	//if a key is pressed, save that in the key array
	m_keys[key] = true;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void InputManager::KeyUp(unsigned key)
{
	//if a key is released, clear the state in the key array
	m_keys[key] = false;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool InputManager::IsKeyDown(unsigned key)
{
	//return key state
	return m_keys[key];
}

