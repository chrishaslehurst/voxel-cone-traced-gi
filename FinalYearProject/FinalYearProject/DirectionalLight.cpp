/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include "DirectionalLight.h"

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

DirectionalLight::DirectionalLight()
{

}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

DirectionalLight::~DirectionalLight()
{

}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void DirectionalLight::SetDiffuseColour(float r, float g, float b, float a)
{
	m_vDiffuseColour = XMFLOAT4(r, g, b, a);
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void DirectionalLight::SetDirection(float x, float y, float z)
{
	m_vDirection = XMFLOAT3(x, y, z);
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

DirectX::XMFLOAT4 DirectionalLight::GetDiffuseColour()
{
	return m_vDiffuseColour;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

DirectX::XMFLOAT3 DirectionalLight::GetDirection()
{
	return m_vDirection;
}

